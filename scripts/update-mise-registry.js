#!/usr/bin/env bun

const REGISTRY_URL = "https://mise.jdx.dev/registry.html#tools";
const CORE_TOOLS_URL = "https://raw.githubusercontent.com/jdx/mise/refs/heads/main/docs/core-tools.md";
const BACKENDS_URL = "https://raw.githubusercontent.com/jdx/mise/refs/heads/main/docs/dev-tools/backends/index.md";
const REGISTRY_TREE_URL = "https://api.github.com/repos/jdx/mise/git/trees/main?recursive=1";
const REGISTRY_SOURCE_URL = "https://github.com/jdx/mise/tree/main/registry";

const OUTPUTS = {
  overview: new URL("../mise/references/installable-overview.md", import.meta.url),
  coreTools: new URL("../mise/references/core-tools.md", import.meta.url),
  backends: new URL("../mise/references/backends.md", import.meta.url),
  registryTools: new URL("../mise/references/registry-tools.md", import.meta.url),
};

async function fetchText(url, accept = "text/plain, text/markdown;q=0.9, */*;q=0.8") {
  const response = await fetch(url, {
    headers: {
      "user-agent": "agent-skills/update-mise-registry",
      accept,
    },
  });

  if (!response.ok) {
    throw new Error(`Failed to fetch ${url}: ${response.status} ${response.statusText}`);
  }

  return await response.text();
}

function escapeCell(value) {
  return String(value).replace(/\|/g, "\\|");
}

function stripBadge(value) {
  return value.replace(/\s*<Badge[^>]*\/>/g, "").trim();
}

function parseMarkdownLinks(markdown) {
  const items = [];

  for (const line of markdown.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed.startsWith("- [")) {
      continue;
    }

    const linkMatch = trimmed.match(/^- \[([^\]]+)\]\(([^)]+)\)/);
    if (!linkMatch) {
      continue;
    }

    const [, name, link] = linkMatch;
    const badgeMatch = trimmed.match(/<Badge[^>]*text="([^"]+)"\s*\/>/);
    let remainder = trimmed.slice(linkMatch[0].length).trim();
    remainder = stripBadge(remainder);

    let note = "";
    if (remainder.startsWith("(") && remainder.endsWith(")")) {
      note = remainder.slice(1, -1).trim();
    }

    items.push({
      name,
      link,
      badge: badgeMatch?.[1] ?? "",
      note,
    });
  }

  return items;
}

function parseCoreTools(markdown) {
  return parseMarkdownLinks(markdown).map((item) => ({
    ...item,
    badge: item.badge || "stable",
  }));
}

function parseBackends(markdown) {
  return parseMarkdownLinks(markdown).map((item) => ({
    ...item,
    badge: item.badge || "stable",
  }));
}

async function getRegistryPaths() {
  const response = await fetch(REGISTRY_TREE_URL, {
    headers: {
      "user-agent": "agent-skills/update-mise-registry",
      accept: "application/vnd.github+json",
    },
  });

  if (!response.ok) {
    throw new Error(`Failed to fetch registry tree: ${response.status} ${response.statusText}`);
  }

  const data = await response.json();
  return data.tree
    .filter((entry) => entry.type === "blob" && entry.path.startsWith("registry/") && entry.path.endsWith(".toml"))
    .map((entry) => entry.path)
    .sort((a, b) => a.localeCompare(b));
}

async function getRegistryItems() {
  const paths = await getRegistryPaths();
  return paths.map((path) => ({
    short: path.split("/").pop().replace(/\.toml$/, ""),
    registryPath: path,
  }));
}

function buildInitialCounts(items) {
  const counts = new Map();
  for (const item of items) {
    const initial = item.short[0]?.toLowerCase() ?? "?";
    counts.set(initial, (counts.get(initial) ?? 0) + 1);
  }
  return [...counts.entries()].sort((a, b) => b[1] - a[1] || a[0].localeCompare(b[0]));
}

function renderOverview({ registryItems, coreTools, backends }) {
  const generatedAt = new Date().toISOString();

  return [
    "# Installable Overview",
    "",
    "This file is generated by `bun scripts/update-mise-registry.js`. Do not edit it by hand.",
    "",
    `- Registry page: ${REGISTRY_URL}`,
    `- Core tools doc: ${CORE_TOOLS_URL}`,
    `- Backends doc: ${BACKENDS_URL}`,
    `- Registry source tree: ${REGISTRY_SOURCE_URL}`,
    `- Generated: ${generatedAt}`,
    "",
    "## Summary",
    "",
    "| Category | Count | Reference | Notes |",
    "|---|---:|---|---|",
    `| Registry aliases | ${registryItems.length} | \`references/registry-tools.md\` | Default shorthand names from the mise registry |`,
    `| Core tools | ${coreTools.length} | \`references/core-tools.md\` | Built-in first-party tools shipped with mise |`,
    `| Backends | ${backends.length} | \`references/backends.md\` | Installation ecosystems and package sources |`,
    "",
    "## How To Use",
    "",
    "- Start with `references/core-tools.md` if you want first-party languages built into mise.",
    "- Read `references/backends.md` when you need to choose a package ecosystem like `npm:`, `pipx:`, `cargo:`, or `github:`.",
    "- Read `references/registry-tools.md` when you want the exact installable shorthand names accepted by `mise use`.",
    "- If a tool is missing from the registry alias list, install it with a full backend-qualified name.",
    "",
  ].join("\n");
}

function renderCoreTools(coreTools) {
  return [
    "# Core Tools",
    "",
    "This file is generated by `bun scripts/update-mise-registry.js`. Do not edit it by hand.",
    "",
    `- Source: ${CORE_TOOLS_URL}`,
    `- Count: ${coreTools.length}`,
    "",
    "## Built-In Tools",
    "",
    "| Tool | Status | Link |",
    "|---|---|---|",
    ...coreTools.map((tool) => `| ${escapeCell(tool.name)} | ${escapeCell(tool.badge)} | ${escapeCell(tool.link)} |`),
    "",
  ].join("\n");
}

function renderBackends(backends) {
  return [
    "# Backends",
    "",
    "This file is generated by `bun scripts/update-mise-registry.js`. Do not edit it by hand.",
    "",
    `- Source: ${BACKENDS_URL}`,
    `- Count: ${backends.length}`,
    "",
    "## Available Backends",
    "",
    "| Backend | Status | Notes | Link |",
    "|---|---|---|---|",
    ...backends.map((backend) => {
      const notes = backend.note || "-";
      return `| ${escapeCell(backend.name)} | ${escapeCell(backend.badge)} | ${escapeCell(notes)} | ${escapeCell(backend.link)} |`;
    }),
    "",
  ].join("\n");
}

function renderRegistryTools(registryItems) {
  const initialCounts = buildInitialCounts(registryItems);

  return [
    "# Registry Tools",
    "",
    "This file is generated by `bun scripts/update-mise-registry.js`. Do not edit it by hand.",
    "",
    `- Source: ${REGISTRY_URL}`,
    `- Registry source tree: ${REGISTRY_SOURCE_URL}`,
    `- Count: ${registryItems.length}`,
    "",
    "## How To Use",
    "",
    "- Run `mise use <short>` with the aliases in this file.",
    "- Use the source path to inspect the corresponding upstream registry definition when needed.",
    "",
    "## Alias Distribution",
    "",
    "| Initial | Aliases |",
    "|---|---:|",
    ...initialCounts.map(([name, count]) => `| ${escapeCell(name)} | ${count} |`),
    "",
    "## Installable Registry Aliases",
    "",
    "| Short | Registry Source |",
    "|---|---|",
    ...registryItems.map((item) => `| ${escapeCell(item.short)} | ${escapeCell(item.registryPath)} |`),
    "",
  ].join("\n");
}

const [coreMarkdown, backendsMarkdown, registryItems] = await Promise.all([
  fetchText(CORE_TOOLS_URL),
  fetchText(BACKENDS_URL),
  getRegistryItems(),
]);

if (registryItems.length === 0) {
  throw new Error("No registry items were found");
}

const coreTools = parseCoreTools(coreMarkdown);
const backends = parseBackends(backendsMarkdown);

await Promise.all([
  Bun.write(OUTPUTS.overview, renderOverview({ registryItems, coreTools, backends })),
  Bun.write(OUTPUTS.coreTools, renderCoreTools(coreTools)),
  Bun.write(OUTPUTS.backends, renderBackends(backends)),
  Bun.write(OUTPUTS.registryTools, renderRegistryTools(registryItems)),
]);

console.log(
  `Updated installable overview files with ${registryItems.length} registry aliases, ${coreTools.length} core tools, and ${backends.length} backends`,
);
